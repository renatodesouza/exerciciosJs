// Utilizando o if

if(10 > 5){
    console.log(10);
}
else{
    console.log(5);
}

// Utilizando operador ternario (>, ?, :)

console.log(10 > 5 ? 10 : 5)

console.log(10 < 15 ? 10 : 5)

console.log(10 >= 5 ? 10 : 5)

console.log(10 <= 5 ? 10 : 5)

console.log(10 == 10 ? 10 : 5)

console.log(10 != 5 ? 10 : 5)